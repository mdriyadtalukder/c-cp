#include <iostream>
using namespace std;
main()
{

    // for
    for (int i = 0; i <= 3; i++)
    {
        cout << "Hello" << i << endl;
    }

    // white
    int j = 0;
    while (j <= 3)
    {
        cout << j << endl;
        j++;
    }

    // do while
    int a = 0;
    do
    {
        cout << "Bangladesh" << endl;
        a++;
    } while (a <= 3);
}